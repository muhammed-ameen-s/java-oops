class human {
  private int age;
  private String name;
  private String mail;
  private int number;

  public int getage() {
    return age;
  }

  public void setage(int age) {
    this.age = age;
  }

  public String getname() {
    return name;
  }
  public void setname(String name) {
    this.name = name;
  }

  public String getmail() {
    return mail;
  }

  public void setmail(String mail) {
    this.mail = mail;
  }

  public int getnumber() {
    return number;
  }

  public void setnumber(int number) {
    this.number = number;
  }
}